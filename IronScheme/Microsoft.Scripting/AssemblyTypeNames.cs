    internal struct TypeName {
        string _namespace;
        string _typeName;

        internal TypeName(Type type) {
            Debug.Assert(!ReflectionUtils.IsNested(type));
            _namespace = type.Namespace;
            _typeName = type.Name;
        }

        internal TypeName(string nameSpace, string typeName) {
            _namespace = nameSpace;
            _typeName = typeName;
        }

        internal string Namespace { get { return _namespace; } }
        internal string Name { get { return _typeName; } }
    }

    internal class AssemblyTypeNames {
        internal static IEnumerable<TypeName> GetTypeNames(Assembly assem) {
#if !SILVERLIGHT
            AssemblyName assemblyName = new AssemblyName(assem.FullName);
            switch (assemblyName.Name) {
                case "mscorlib": return Get_mscorlib_TypeNames();
                case "System": return Get_System_TypeNames();
                case "System.Xml": return Get_SystemXml_TypeNames();
                case "System.Drawing": return Get_SystemDrawing_TypeNames();
                case "System.Windows.Forms": return Get_SystemWindowsForms_TypeNames();
            }
#endif

            Type[] types = LoadTypesFromAssembly(assem);

            return GetTypeNames(types);
        }

        static IEnumerable<TypeName> GetTypeNames(Type[] types) {
            foreach (Type t in types) {
                if (ReflectionUtils.IsNested(t)) continue;
                TypeName typeName = new TypeName(t);
                yield return typeName;
            }
        }

        static Type[] GetAllTypesFromAssembly(Assembly asm) {
#if SILVERLIGHT // ReflectionTypeLoadException
            try {
                return asm.GetTypes();
            } catch (Exception) {
                return ArrayUtils.EmptyTypes;
            }
#else
            try {
                return asm.GetTypes();
            } catch (ReflectionTypeLoadException rtlException) {
                return rtlException.Types;
            }
#endif
        }

        internal static Type[] LoadTypesFromAssembly(Assembly asm) {
            if (ScriptDomainManager.Options.PrivateBinding) {
                return GetAllTypesFromAssembly(asm);
            }

            try {
                return asm.GetExportedTypes();
            } catch (NotSupportedException) {
                // GetExportedTypes does not work with dynamic assemblies
            } catch (Exception) {
                // Some type loads may cause exceptions. Unfortunately, there is no way to ask GetExportedTypes
                // for just the list of types that we successfully loaded.
            }

            Type[] allTypes = GetAllTypesFromAssembly(asm);

            Predicate<Type> isPublicTypeDelegate = delegate(Type t) { return t != null && t.IsPublic; };
            return ArrayUtils.FindAll(allTypes, isPublicTypeDelegate);
        }

#if !SILVERLIGHT
        static IEnumerable<TypeName> GetTypeNames(string [] namespaces, string [][] types, TypeName [] orcasTypes) {
            Debug.Assert(namespaces.Length == types.Length);

            for (int i = 0; i < namespaces.Length; i++) {
                for (int j = 0; j < types[i].Length; j++) {
                    TypeName typeName = new TypeName(namespaces[i], types[i][j]);
                    yield return typeName;
                }
            }

            if (IsOrcas) {
                foreach(TypeName orcasType in orcasTypes) {
                    yield return orcasType;
                }
            }
        }

        static bool IsOrcas {
            get {
                Type t = typeof(object).Assembly.GetType("System.DateTimeOffset", false);
                return t != null;
            }
        }

